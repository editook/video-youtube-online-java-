    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if (txtUsuario.getText().equals("") || pwsUsuario.getText().equals("")) {
            txtUsuario.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            pwsUsuario.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            JOptionPane.showMessageDialog(this, "Ingrese Todos los campos");
        }
        if (txtUsuario.getText().equals("admin") && pwsUsuario.getText().equals("admin")) {
            Interfaz_admin admin = new Interfaz_admin(this);
            admin.setVisible(true);
            this.setVisible(false);
        }
        else{
            boolean respuestaPeticion=registrar.getUsuarios().esUsuario(txtUsuario.getText(),pwsUsuario.getText());
        if (respuestaPeticion) {
            Interfaz_usuario usuario = new Interfaz_usuario(txtUsuario.getText());
            usuario.setVisible(true);
            this.dispose();
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "Usuario no resgistrado");
        }
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

